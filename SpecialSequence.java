public class SpecialSequence {
    public static void main(String[]agrs){

        // it inserts a tab space
        String str = "Tony\tStark";
        System.out.println(str);

        // it inserts a new line
        String str1 = "if you can dream it \n you can do it";
        System.out.println(str1);

        // it inserts a backslash
        String str2 = "AND\\OR";
        System.out.println(str2);

        // it insert a carriage
        String str3 = "Carriage\rreturn";
        System.out.println(str3);

        // it prints a single quote
        String str4="Wall street\'s";
        System.out.println(str4);

        // it prints a double quote
        String str5="Pratima\'She is Beautiful'and honest";
        System.out.println(str5);



    }

}